
/** OPERADORES DE COMPARACION 
 * >, a>b
 * <, z<b
* >=, a>=b
* <=, z<=b
==, a==b
!=, a!=b
operador estricto: ===
operador distinto estricto: !==
*/


let num1=22
let num2=15

console.log(num1+ "<"+ num2+ " "+ (num1<num2));
